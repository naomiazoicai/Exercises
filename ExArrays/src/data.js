function top5Countries() {
    const arr = getOlympicData();
    return arr
        .sort((a, b) => b.Population - a.Population)
        .slice(0, 5)
        .map(item => item.Nation);
}

function countriesStartingWithA() {
    const arr = getOlympicData();
    return arr
        .filter(item => item.Exists === "YES" && item.Nation.startsWith('A'))
        .map(item => item.Nation);
}

function totalPopulation() {
    const arr = getOlympicData();
    return arr.reduce((sum, item) => sum + item.Population, 0);
}

function top5EarliestAppearances() {
    const arr = getOlympicData();
    return arr
        .sort((a, b) => a.First_App - b.First_App)
        .slice(0, 5)
        .map(item => item.Nation);
}

function nationCodeArray() {
    const arr = getOlympicData();
    return arr.map(item => ({
        nation: item.Nation,
        code: item.Code
    }));
}

function countryWithMostAppearances() {
    const arr = getOlympicData();
    return arr.reduce((oldest, item) =>
        item.First_App < oldest.First_App ? item : oldest
    ).Nation;
}


function athleticsCountries() {
    const arr = getOlympicData();
    return arr
        .filter(item => item.MostSuccessfulSport === "Athletics")
        .map(item => item.Nation);
}

function countryPopulationObj() {
    const arr = getOlympicData();
    return arr.reduce((obj, item) => {
        obj[item.Nation] = item.Population;
        return obj;
    }, {});
}

function countriesGroupedByLetter() {
    const arr = getOlympicData();
    return arr.reduce((acc, item) => {
        const firstLetter = item.Nation[0].toUpperCase();
        if (!acc[firstLetter]) acc[firstLetter] = [];
        acc[firstLetter].push(item.Nation);
        return acc;
    }, {});
}

function randomCountryInfo() {
    const arr = getOlympicData();
    const randomItem = arr[Math.floor(Math.random() * arr.length)];
    return {nation: randomItem.Nation, population: randomItem.Population};
}


function moreWinterThanSummerMedals() {
    const arr = getOlympicData();
    return arr
        .filter(item => item.Population > 1000000 && item.W_Medal > item.S_Medal)
        .map(item => item.Nation);
}

function smallCountriesWithAboveAverageMedals() {
    const arr = getOlympicData();
    const totalMedals = arr.reduce((sum, item) => sum + item.Medals, 0);
    const averageMedals = totalMedals / arr.length;

    return arr
        .filter(item => item.Population < 5000000 && item.Medals > averageMedals / 2)
        .map(item => item.Nation);
}


function countryWithMostRecentAppearance() {
    const arr = getOlympicData();
    return arr.reduce((mostRecent, item) =>
        item.First_App > mostRecent.First_App ? item : mostRecent
    ).Nation;
}

function oldestCountriesThatStillExist() {
    const arr = getOlympicData();
    const oldestYear = Math.min(...arr.map(item => item.First_App));
    return arr
        .filter(item => item.First_App === oldestYear && item.Exists === "YES")
        .map(item => item.Nation);
}


